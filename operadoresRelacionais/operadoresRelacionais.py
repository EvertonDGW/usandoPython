"""
Operadores Relacionais
== (igual a)
!= (diferente de)
> (maior que)
< (menor que)
>= (maior ou igual a)
<= (menor ou igual a)

"""
print('10 é igual a 10?')
a = 10
b = 10
resultado = (a == b)
print(resultado)  # Saída: True

print('10 é diferente de 5?')
a = 10
b = 5
resultado = (a != b)
print(resultado)  # Saída: True

print('10 é maior que 5?')
a = 10
b = 5
resultado = (a > b)
print(resultado)  # Saída: True

print('5 é menor que 10?')
a = 5
b = 10
resultado = (a < b)
print(resultado)  # Saída: True

print('10 é maior ou igual a 10?')
a = 10
b = 10
resultado = (a >= b)
print(resultado)  # Saída: True

print('5 é menor ou igual a 10?')
a = 5
b = 10
resultado = (a <= b)
print(resultado)  # Saída: True