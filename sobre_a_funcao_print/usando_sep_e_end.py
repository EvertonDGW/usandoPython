"""
sep e end são parametros da funcao print()
"""

# sep "" : é usado para remover os espaços vazio entre os numeros, ou seja, vai juntar os numeros
# end '\n': é usado para quebra de linha
print(12, 34, 1011, sep="", end='\n') 

# sep '-' : o hifen esta sendo colocar entre os numeros
# end '#' b: é usado para adicionar o simbolo de # ao final do print
print(56, 78, sep='-', end='#')


# conclusão: sep é usado para inserir ou remover algo entre valores, enquanto end é usado para inserir ou remoever algo ao final do print