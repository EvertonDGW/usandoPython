# no python todo e qualquer numero INTEIRO positivo ou negativo sem ponto flutuante é considerado type = int
# no python todo e qualquer numero com PONTO FLUTUANTE (virgula) positivo ou negativo é considerado type = float

# type: e um função do python que mostra o tipo do objeto, se ele é uma string, number etc...
print(type('everton')) # str, ou seja, uma string
print(type(0)) # int, ou seja, um numero inteiro
print(type(1)) # int, ou seja, um numero inteiro
print(type(-1)) # int, ou seja, um numero inteiro

print(type(0.0)) # float, ou seja, um numero de ponto flutuante, ou seja, um numero com virgula
print(type(1.1)) # float, ou seja, um numero de ponto flutuante, ou seja, um numero com virgula
print(type(-1.1)) # float, ou seja, um numero de ponto flutuante, ou seja, um numero com virgula
