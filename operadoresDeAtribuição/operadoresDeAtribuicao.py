# Atribuição de Adição: +=

x = 5
x += 3  # Equivalente a x = x + 3
print(x)  # Resultado: 8

# Atribuição de Subtração: -=
x = 5
x -= 2  # Equivalente a x = x - 2
print(x)  # Resultado: 3

# Atribuição de Multiplicação: *=
x = 5
x *= 4  # Equivalente a x = x * 4
print(x)  # Resultado: 20

# Atribuição de Divisão: /=
x = 10
x /= 2  # Equivalente a x = x / 2
print(x)  # Resultado: 5.0

# Atribuição de Divisão Inteira: //=
x = 10
x //= 3  # Equivalente a x = x // 3
print(x)  # Resultado: 3

# Atribuição de Módulo: %=
x = 10
x %= 3  # Equivalente a x = x % 3
print(x)  # Resultado: 1

#Atribuição de Exponenciação: **=
x = 2
x **= 3  # Equivalente a x = x ** 3
print(x)  # Resultado: 8

# Atribuição de Bitwise AND: &=
x = 6  # Em binário: 110
x &= 3  # Em binário: 011, resultado será 010 (em decimal: 2)
print(x)  # Resultado: 2

# Atribuição de Bitwise OR: |=
x = 6  # Em binário: 110
x |= 3  # Em binário: 011, resultado será 111 (em decimal: 7)
print(x)  # Resultado: 7

# Atribuição de Bitwise XOR: ^=
x = 6  # Em binário: 110
x ^= 3  # Em binário: 011, resultado será 101 (em decimal: 5)
print(x)  # Resultado: 5

# Atribuição de Shift à Esquerda: <<=
x = 2  # Em binário: 10
x <<= 2  # Desloca os bits duas posições à esquerda (equivale a multiplicar por 2^2)
print(x)  # Resultado: 8 (em binário: 1000)

# Atribuição de Shift à Direita: >>=
x = 8  # Em binário: 1000
x >>= 2  # Desloca os bits duas posições à direita (equivale a dividir por 2^2)
print(x)  # Resultado: 2 (em binário: 10)